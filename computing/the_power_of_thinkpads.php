<p>
  The build quality is still way better than any laptop nowadays. I had purchased mine used off of eBay in 2014 or 2015 for $200 dollars from some computer store account. I used it as my main device all the way until the summer of 2019 when the Intel graphics chip finally died on me (it was faulty from the day I got it). It was mostly pristine the entire time I had it, aside from one of the hinges breaking a few months in, something I replaced quickly and cheaply for $5 (CAD) from China off of eBay. I would have had it still if it wasn't for that GFX chip. Pretty damn good for a laptop being used and abused in an office environment since 2008.
</p>

<p>
  At the time, I was running Ubuntu 14.04/16.04, Arch Linux, and Debian 7, 8, and 9. My main OS was Debian. I always ran <code>i3wm</code>, only dabbling with <code>dwm</code> towards the far T500s end of life. I only used terminal based programs, aside from Firefox and a few others that were mandatorily GUI. The T500 felt snappier than any new laptop on the market, totally because of 1) the great keyboard (not the flat, chicklet trash we have nowadays), and 2) the great TrackPoint system of ThinkPads. This is only counting hardware, I'm not counting the great software that is tiling managers. You don't need those modern spyware machines when you use good software.
</p>

<p>
  The laptop was libreboot and quadcore capable, and with a detachable CPU. What kind of laptop has a removable CPU now? Mostly none. Good doesn't appeal to the general public, as they have no need for removable CPUs and such. They prefer the sheen of a MacBook, or the so-called "ease of use" of a Microsoft Surface. It's unfortunate.
</p>

<p>
  From my research, this was the peak of old ThinkPads, which I still believe. Actually, even more so, the peak laptop. If you're looking for a big laptop, one that's customizable, one that's free from the constrictions of proprietary software, and know how to use a computer and surpass its hardware incapabilities, buy this. If you want a super small one, buy a laptop from the ThinkPad X-Series. That's what I'll buy next in 2 to 4 years, after I my E550 breaks or if I get bored of it and have enough disposable income.
</p>

