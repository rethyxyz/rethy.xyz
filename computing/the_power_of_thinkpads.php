<h2>My (Current) E550</h2>
<p><center><img src="files/E550.jpg" width=50% /></center></p>

<ul>
<li>Intel Core i3 (not sure exactly which, will update when I bother to look)</li>
<li>Intel HD Graphics</li>
<li>8GB DDR3L 1600 MHz (upgraded from 4GB)</li>
<li>15.6" HD 1366 x 768 200 nit Antiglare</li>
<li>14.84" x 10.08" x 1.06"</li>
<li>720P HD Webcam with dual mic (I put tape over mine, I don't use it)</li>
<li>240 GB Kingston SSD and 1TB Seagate HDD (replaced CD drive with HDD bay)</li>
</ul>

<p>Right now I own a E550 (two to be exact (one I keep for parts and loan to a family member)), which is good, but not as good as the old style and feel of my T500. I ended up with the E550 through one of my parents workplace, who previously had an entire supply of Lenovo products at their office. Recently (2019), upon upgrading, they had a surplus of Lenovo products and knew that I fixed computers, so they gave them to me, knowing that I'll put them to good use. I was given two ThinkCenters and two ThinkPad E550s to be exact. One of the ThinkCenters was old, using DDR2 ram, the other being a very new DDR3 system with lots of SATA ports (4, I think).</p>

<p>I normally run <code>dwm</code>, <code>i3wm</code>, or Windows if I'm messing with Cyber security/pen-testing stuff. I used to hate running Windows, as I'm better with Linux, but I've gotten used to it by using AutoHotkey, WSL, and gVim. These days it's actually super usable. Some days I even enjoy the ease of Windows... Other times not so much. Anyway, seeing as the laptop has 8GB DDR3L, one SSD and a 1TB HDD (swapped CD drive for a hard drive bay), and the glorious TrackPoint (not as good as the T500's), it's not a bad system. Oh, and the battery life is better than the T500. Of course it's usable, it's still a fairly new laptop, but it's not something that I would have purchased if it wasn't for free. The only reason I'm using it now is because I have two of them, and it just works. There's no reason to go and spend money on another system when this one works just fine as it is.</p>

<p>I do have some gripes with the thing: screen is 1336x768 and has bad viewing angles, the keyboard is in the new ThinkPad style (chicklet trash, it feels like you're typing on a board), and no screen hinge lock.</p>

<h2>The Power of ThinkPads</h2>
<p>ThinkPads are peak personal computing. ThinkPad, old and new, trump all other PCs. None other than ThinkPad has customizability, extensibility, and the best pointer system (TrackPoint) all in one, without compromise. To clearly answer the question, the power of ThinkPads lie in their ability to do what the user wants instead of the user conforming to what their system is limited to. In a way, it's like the free (libre) software of the hardware world.</p>

