<h1>About Richard Stallman's Odd Website</h1>
<p><b><small>Created: 2021-02-20 | Modified: 2021-02-20</small></b></p><hr>

<p><b>Note</b> I am not ridiculing either him or the things he shares on his personal website. I think that it's all great. Even more so, I enjoy that he shares the parts of life that some would see as odd. I'm hoping that more people act as he does. We, as people, still aren't completely genuine with others, but are moving in that direction by breaking societal norms more and more. I'm hopeful.</p>

<p>RMS (refering to Richard Stallman's alias/username) has a website full of some really interesting things. By interesting I mean odd. Not bad, just odd. They typically aren't things that normal people would be sharing, but Richard Stallman is far from normal.</p>

<p>He's a renown figure for this, among other things. Incase you don't know, this is the big one:</p>

<blockquote>Richard Matthew Stallman, born March 16, 1953, also known by his initials, rms, is an American free software movement activist and programmer. He campaigns for software to be distributed in a manner such that its users receive the freedoms to use, study, distribute, and modify that software. Software that ensures these freedoms is termed free software. Stallman launched the GNU Project, founded the Free Software Foundation, developed the GNU Compiler Collection and GNU Emacs, and wrote the GNU General Public License.</blockquote>

<blockquote>Stallman pioneered the concept of copyleft, which uses the principles of copyright law to preserve the right to use, modify, and distribute free software, and is the main author of free software licenses which describe those terms, most notably the GNU General Public License (GPL), the most widely used free software license.</blockquote>

<p>Taken from <a href="https://en.wikipedia.org/wiki/Richard_Stallman">Wikipedia</a>. Now you know.</p>

<p>His website is a gem. Below I'll add some of his indexes verbatim. Please enjoy, as I did reviewing them.</p>



