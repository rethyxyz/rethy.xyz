<p><b>Charli XCX</b>. <b>True Romance</b>. 2013. Named after the Quentin Tarantino movie by the same name.</p>

<p>A fucking fantastic synth pop/post-trance album. So far the most unbeatable of her discography.</p>

<p>Powerful synth music with obvious pop elements. This is really viewable in the lyrics. Y'know, it's that romantic stuff. I don't completely dislike it, though. Usually I depise all mention of the cringe worthy "don't leave me" kind of thing, but she does it in a different way, focusing on other aspects of a romantic relationship. It isn't mind blowing, but it's a good switch up.</p>

<p>Really strong intro, flowing one into another for the first three tracks, of which are the album best. Everytime I come back, I start with the first three tracks (<b>Nuclear Seasons</b>, <b>You</b>, <b>Take My Hand</b>), sometimes skipping the fourth (<b>Stay Away</b>) depending how I'm feeling, and going right to the 7th (<b>So Far Away</b>), where I stop and do something else. I think this album suffers from the typical album creation burnout type of thing. It starts strong, but fizzles out in the end. It's a shame, but happens all too often during the creation of musical projects.</p>

<p>The end is so bad. It sounds like they didn't even bother mixing <b>Lock You Up</b>, the final track, which is alright disregarding so. It suffers from shitty main riffage, with a mildly intriguing chorus, and good vocal performance.</p>

