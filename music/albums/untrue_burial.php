<p><i>Burial</i>. <i>Untrue</i>. 2009. The classic album defining the UK city sound (though I don't really know, as I'm from Canada) so I've heard.</p>

<p>My first exposure to Burial came from the album <i>Mt. Eden</i>: <i>Mt Eden Album</i>. It was a remix of <i>Archangel</i>. I liked it from the start. Only in 2014 or so did I discover the origin, <i>Untrue</i>.</p>

<p>Nice album. Categorized by a perfect display of drum programming, and floaty, low-budget vocals. Never full vocals either. Just snippets. Each song is really similar in mixing and overall production value. This isn't a bad thing. The end result is a centered and conceptual album surrounded around clever mixing.</p>

<p>From what I've heard, the creator is an alledged audio engineer, which explains the atypicality of the end mixing and drum production. The alledged creator, William Emmanuel Bevan, made the album in a audio editor called X, not a digital audio workstation (DAW). This is not an easy feat, adding to the overall cult-following surrounding the album.</p>

<p>Many of the samples on the album seems to have been taken from YouTube. Specifically the vocals.</p>

