<p>1989. Disintegration by The Cure. I discovered this album back in college, after having it been recommended by a friend. I could never get into The Cure at this point, but finally understood them after this album.</p>
<p>Prior to hearing this, I had heard Boys Don't Cry, and thought it was pretty good, but didn't think to get into them, even knowing many of the best brit-pop bands were influenced by them (specifically Radiohead, a favorite of mine).</p>
<p>Over the years, I've listened to this album many many many times.</p>

<h2>1. Plainsong</h2>
<p>Good intro to the album, but boring as a song, and doesn't standalone. Title fits. It really is a plainsong.</p>

<h2>2. Pictures of You</h2>
<p>I just skip this song. It feels out of place on the album.</p>

<h2>3. Closedown</h2>
<p>Good song. It feels like a sort of interlude. It's a heavy display of the drumming capabilities of the drummer (I'm not sure who it was, all I know is that it wasn't Tolhurst).</p>

<h2>4. Lovesong</h2>
<p>I like the close up vocals, slight stereo offset, and the level of depth between each drum piece (centered with slight burn into each side), and synths. Super organic feel overall. Acoustic, even. You can even hear the occasional fret buzz, especially at the end.</p>

<h2>5. Last Dance</h2>
<p>Nice balance between the instruments and vocals. I like the moments when the guitar hits the slight droning period, during which you can hear the vocals fully; uninterupted.</p>

<h2>6. Lullaby</h2>
<p>Nice synth strings, plucks, and choppy guitars, accompanying the strings. Subtle background vocals, and small whispers and gruntiled slurps from the spiders point-of-view.</p>
<p>If my memory serves me correct, the song is about Robert Smith's Uncle. He told him scary shit to mess with him, as it was something he himself found amusing.</p>

<h2>7. Fascination Street</h2>
<p>Great introduction bass line with two encroaching guitar leads. Reverbed out drums. Ambient synth stuff too, all in unison, but with their own levels of detail still being presented. Good start to the song, but it goes on, doing more of the same thing. Quite repeditive, but not necessarily a bad thing, as it has enough progressive layering not to bore, even with clear repitition.</p>

<h2>8. Prayers for Rain</h2>
<p>For as long as I can remember, I could never get through the entire song. Later on, I learned it was because of the intro. I got turned off because of the wild west sounding shite. Stay a while. Don't turn it off, because as I've learned, it's not bad. Heavily reverbed bass brings it together towards the middle, but eventually loses my interest.</p>

<h2>9. The Same Deep water as You</h2>
<p>Boring and repeditive. It portrays an emotion, but doesn't sound like my thing. Filler track possibly.</p>

<h2>10. Disintegration</h2>
<p>Self-titled track. Great introduction. Glass crack sound into the best bass line on the album. It hits hard, and goes on and on... It's acommpanied for the first minute only by drums and light synth work. Eventually, a guitar and synth lead in unison joins. At around one minute thirty seconds, the vocals. By far the best vocals. Vocal work keeps itself on the same base note for a while, executing the action of jumping up some few notes every five words. Really heavy sounding. A super strong, standalone piece. By far the best on the album, and in my opinion, one of their best tracks as a band.</p>

<p>This, and Fascination Street are the two songs that keep me coming back to the album. It makes all if this worth it, even among some of the more boring pieces.</p>

<h2>11. Homesick</h2>
<p>Uninteresting.</p>

<h2>12. Untitled</h2>
<p>Good outro, I suppose. Nothing to say about it. To me, the album should have resolved at Disintegration, but I understand that leaving on a high note would be too abrasive for general consumer audiences.</p>

