<p>A really nice folksy indie album from multi-instrumentalist and creative Sufjan Stevens. Illinois is really rich in instrumentation. Unbelievably well though out and deliberate. If my memory serves me correct, it features only organic instruments. No synths, or what have you (bass guitar, yes, but I count that as organic, even though it's amplified).</p>

<p>In my opinion, the pinnacle (and fullest representation) of the album comes at <i>Come On! Feel Illinoise!</i> The song is really a two parter, starting as a fully orchestral and colorful array, merging in the middle to a more downbeat, lyrically depressive song.</p>

