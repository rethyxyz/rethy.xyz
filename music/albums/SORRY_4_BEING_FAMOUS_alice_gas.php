<p>A pretty alright album. Defined as Hyperpop (it's really multigenere, spanning across a multitude of electronic ones).</p>

<p>I wish it was more consistent. It seems half of each song is music, while the remainder is unlistenable distortion, or whatever other abrasive noise they chose. If it wasn't a factor, I'd consider the album to be well mixed. Through, I will give the artist the benefit of the doubt here. I try to do that, as I probably just missed what they were going for with that particular feature.</p>

<p>People refer to <b>FERRARI</b> as the defining song, at least from what I've heard, but I don't think so. For me, it's too humorous, and I rarely choose to listen to it, though it isn't bad. <b>RUN IT UP</b> is what I believe is the best. It features least of the "funny" attitude of the other songs, and doesn't have the unlistenable distortion stuff. It's a good song standalone.</p>

<p>Fuck the cringe fanbase, though. That goes for all Hyperpop.</p>

