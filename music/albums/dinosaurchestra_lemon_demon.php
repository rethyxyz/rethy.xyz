<p>A album guaranteed to be unknown to anyone living outside of the internet during the mid-2000s (even if you were, it's still unlikely).</p>

<p>A nostalgic album from old school internet content creator Neil Cicierega, who has went on to create an whole assortment of music/short films ranging from <i>Harry Potter Puppet Pals</i> to <i>The Ultimate Showdown of Ultimate Destiny</i> (which is found on this album; track #6).</p>

<p>He's least notably-but-still-importantly know for old school computer animations created from collections of GIFs and JPEGs called Animutations. His creations landed him on the news at the age of (I believe) 14 years of age, his first taste of Internet virality.</p>

<p>Structurally, the album is simple. Typical time signatures and rhythms. Most of the outcome is predictable, as you can expect where the next section will go and how it will sort of sound. This isn't a bad thing, as there isn't too much of it. Now, where its inventiveness come in, is through its subject matter and actual instrumentation.</p>

<p>Lets take the lyrics for an example: Weird shit. The song <b>Stuck</b> (track 5) is about Neil (the creator of the album) being stuck in a song (this song, to be specific). It's actually quite humerous. Have a listen.</p>

<p>That's just one example, but mostly every song has this sort of superficial-introspective-ness.</p>

