<p>
    Not a bad album. My introduction into Laleh started last week after seeing her in a YouTube video from a Swedish programmer guy whose community I tend to frequent. He, in the past, had uploaded one of her songs, that I will say I didn't enjoy (it was from her more recent, poppy-er stuff). But, I was intrigued by the thought of listening to an album by a Swedish artist, so here I am.
</p>

<p>
    Overall, not bad. A good example of contemporary Jazz from the 2000s, something I've consumed a lot of and still do to this day. This album, though, is quite atypical from the rest. It utilizes odd track introductions, vocal styles, and repitition, really bringing an interesting combination together. It truly feels like a cohesive piece. Overall, the album felt foreign (literally, it's Swedish), but this very well could have been because I've not heard any current day Swedish albums. Therefore, I wasn't a fan of any of the songs conducted in Swedish. I can see myself getting used to it, though.
</p>

<p>
    I'll definitely end up listening to this again for a few of the song: <b>Live Tomorrow</b>, <b>Hame Baham</b>, and <b>Storebror</b>. Live Tomorrow is my favorite and the best on the album. Most others agree. It's the only real hit from the album, <a href="https://www.youtube.com/watch?v=tu9NPb2G_pM">amassing <b>5.6M</b> on the official YouTube music video</a>.
</p>

