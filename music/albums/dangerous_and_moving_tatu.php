<p>A quick introduction to t.A.T.u. In-case you don't know already, t.A.T.u was a Russian pop collective put together by obscenely rich, old East Slavic speaking men with the purpose of making as much guap off of vulnerable adolescents. The outcome of their money stricken vision was some very heavily produced people classified by persona, impersonal music, and heavily rehearsed (and lip synced) live shows fronted by two teens masquerading (just a persona, in fact one is a public homophobe) as lesbians for the purpose of gaining easy publicity (smart, seeing as primarily marketed towards Russians, whom historically do not have the best acceptance track record).</p>

<p>Because of the high production value of the albums and the sheer amount of human individuals collectivizing to produce each song, the music is what you'd call "good": Overproduced (not in a bad way), and catchy. As far as I know, the two front-women (actually, young teens, as they were only 14 at the time of the weird, hyper-sexualized marketing ploy) did not have any creative input on the direction of the (so-called) band, nor did not know each other prior to the formation of the group.</p>

<p>It may not sound like it from my explanation of the group's history above, but I actually like this album. I've spent countless hours on it. Surprisingly enough, the music is quite atypical, at least from a North American point-of-view (I'm not sure if all Russian pop from the early-2000s sounds like this). Use of lead synths, synth bass lines, and digitized drum tracks are prevalent throughout. Sweet album.</p>

