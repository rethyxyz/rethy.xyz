<p>I recently watched a documentary about Andrew Wood, a pioneer of the original grunge scene in Seattle, Washington.</p>

<p>The documentary focused on his life specifically, but also included information on his first band, Malfunkshun, and his second, Mother Love Bone, being more well known. The latter is known as arguably the first breakout band of what is now known as the grunge scene, albeit short lived. Almost all other original grunge bands (Nirvana, Soundgarden (whom the lead singer, Chris Cornell, lived with Wood), Mudhoney, The Melvins) have been inspired or influenced by either or both of their bands.</p>

<p>The documentary focused in Wood's personality, being proclaimed as "quite the character". During the time in his first band, Malfunkshun, he commonly dressed up in flashy female garb and sported makeup, breaking gender norms, something he was known for locally. He did away with this after the inception of his second band. His purpose was quite simply fun, nothing else, really.</p>

<p>They also went into his many addictions and downfalls, which, eventually, most unfortunately, randomly, and suddenly, let to his death from Heroin overdose.</p>

<p>Overall, I enjoyed the documentary, even though I don't think his second band, Mother Love Bone, was anything special, sounding like glam rock meets dad rock. I did enjoy the piano based songs from their first album, though, as I did with his first bands.</p><br>

