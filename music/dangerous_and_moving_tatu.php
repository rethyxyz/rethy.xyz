<p>This album is way better than their first. I've heard it way more, enjoy it way more, and believe it's just overall better. If the album had <b>All the Things She Said</b>, the single off of their first (and previous) album, Dangerous and Moving would be perfect.</p>

<p>A quick introduction to T.A.T.U: In-case you don't know, T.A.T.U. was a Russia pop collective put together by obsenely rich old Eastern European men with the purpose making as much money as possible off of vulnerable teenagers. The outcome of their vision was some very heavily produced personas, music, videos, and live shows fronted by two teens masquerading as lesbians (just a persona, in fact one is a public homophobe) for the purpose of generating as much publicity (which makes sense, seeing as they were marketed towards young Russians during the early 2000s) as possible.</p>

<p>Because of the high production value of the albums and the sheer amount of human individuals producing songs together, the music is what you'd call good. Overproduced (not in a bad way) and catchy. As far as I know, the two front-women (or should I say girls as they were 14 or so at the time of the weirdly oversexualized marketing ploy) did not have any creative input over the direction of the (so-called) duo, and did not know each other prior to the formation of the group.</p>

<p>It may not sound like it from my explanation of the group's history above, but I actually like the album. Surprisingly enough, the music is quite atypical, at least from a North American point-of-view (I'm not sure if all Russian pop from the early-2000s sounds like this). Use of leader synths, synth basslines, and computerized drum tracks are prevalent throughout.</p>

<p>Favorite track: <b>Perfect Enemy</b> or <b>Dangerous and Moving</b>.</p>

