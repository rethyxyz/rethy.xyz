<h2>Comfort</h2>
<p>Koss's Headphones are super-lightweight, mainly because of the tiny amount of material used for the frame of the headphones. The headphones I mentioned are really just two headphone drivers within a thin plastic enclosure. The Porta Pro sits lightly on your head, not gripping tightly, as not to cause strain. The KSC75 clips on your ear, which is the most comfortable for me, but some others with larger ears say otherwise. I can wear the KSC75 for hours on end and usually forget I'm wearing them.</p>

