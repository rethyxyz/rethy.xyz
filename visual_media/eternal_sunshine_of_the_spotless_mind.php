<p>As are most things by Charlie Kaufman (the creator of this film), this film is gold. It's really good. I've watched it upwards of, probably 20 times. It was my favorite for a long time, too.</p>

<p>The two front people: Comedy's Jim Carrey, and pop-actress Kate Winslet. I cannot speak for Kate Winslet as I haven't seen her other works, but Jim Carrey is terribly underrated during his roles in more serious movies.</p>

<p>At a young age, I had been introduced to Jim Carrey through his mediocre comedy films. After getting to an age of choice and pseudo-sentience, say 10 or so, I recognized that comedy movies are boring trash. Even among the notoriously bad comedies, I could tell he was a good actor (probably not at 10, but later on). This movie proves he has aptitude for not just cliche comedies (The Truman Show proves this as well).</p>

<p>Back to the point: This film is good. It conducts itself in a non-linear fashion starting around 20 minutes in. It doesn't get confusing whatsoever. Good cinematography, being inventive while keeping a clear and defined structure. This film also uses ASMR as a sort of device to intrigue.</p>

