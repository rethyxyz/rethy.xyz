<p>About dead people who live in a limbo dimension at a high school. These people end up fighting against a supposed angel who (they claim) want to make them disappear from this dimension. They fight the angel to stay in this dimension. They don't know what happens when they die there, and they don't want to find out. That's really all you need to know.</p>

<p>Classic late 2000s anime. If you're anyone who reads anime forums or charts, you've probably seen someone mention this, or have seen it yourself.</p>

<p>The typical episode run does goes as follows: The Protagonist wakes up, goes to class, during/after class learns a sub-main character's sob story, the whole team does a "raid" to get food, items, etc, and the day completes on a positive note. I make it sound mundane, but the smaller things throughout are what make it so good.</p>

<p><center><iframe src="https://www.youtube.com/watch?v=fORH60LtOt4"></iframe></center></p>

<p><b>Unpredictably funny</b>. I usually don't care for that classic anime style banter, as I feel like it doesn't translate into English speaking attitudes well, but this show is actually funny. The best being - and the only one that stuck with me till now - is the rocket propulsion scenes. They may not be funny on their own, as the unexpectedness is that makes the joke, but you can watch it above.</p>

<p>Good overall, bad ending. It wasn't realistic, and was likely made so they could carry on the show more sequels (as shown by the numerous trashy sequels, which I likely won't be watching).</p>

