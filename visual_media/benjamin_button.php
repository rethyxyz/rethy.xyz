      <p>The romantic portions were too much for me, but as I said, I recognize I'm not the target demographic, nor will I be ever be.</p>

      <p>I did enjoy the portions during the portrayal of his early life. It eventually got into his teens (in age, not in physical appearance), which is when he started working on a tugboat led by an interesting captain. This captain and he became close friends until he died in the war alongside Benjamin (the main character). As I see it, this captain character was a device used to bring the protagonist into maturity. This was an interesting time in the movie, but things progressively got worse. It's like they forgot that he aged backwards and turned the entire thing into a sappy romance novel, only once again remembering his condition at the very end. It was alright, but not worth the hype. The acting was good, though. No issues with that. Just bad execution of the story.</p>

