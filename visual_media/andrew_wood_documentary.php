<p>I recently watched a documentary about Andrew Wood, a pioneer of the original grunge scene in Seattle, Washington.</p>

<p>The documentary focused on his life specifically, but also included information on his first band, Malfunkshun, and his second, Mother Love Bone, being more well known. The latter is known as arguably the first breakout band of what is now known as the grunge scene, albeit short lived. Almost all other grunge bands, Nirvana, Soundgarden (whom the lead singer, Chris Cornell, lived with Wood), Mudhoney, The Melvins, have been inspired or influenced by either of their bands.</p>

<p>They got into Wood's personality, being quite the character. In his first band, he commonly dressed up in flashy female garb and sported makeup, breaking gender norms, something he was known for locally. He did away with this after starting his second band. His purpose for doing this was fun, nothing else, really.</p>

<p>Overall, I enjoyed the documentary, even though I don't think his second band, Mother Love Bone, was anything special, sounding like glam rock meets dad rock. I did enjoy the piano based songs from their first album, though, as I did with his first bands.</p><br>

