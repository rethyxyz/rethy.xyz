<h3>Pre-Roman Gaul (Hallstatt C-D)</h3>
<p>There is little information on Pre-Roman Gauls aside from what has been gathered through archaeology, mainly from coins. But, as stated in the summary, they lived in small tribal farming villages, were highly-advanced (metal workers) and proficient traders with many (especially after Roman contact).</p>

