<p>A period emphasizing social distinction, in an age of clashing elite classes of chieftains and warriors.</p>

<h3>Languages</h3>
<p>Most if not all languages spoken were Celtic-type.</p>

<h3>Civilizations</h3>
<p>
Categorized as:
<ul>
<li>Forts situated on hill tops</li>
<li>Approximately the size of a small modern village</li>
<li>Workshops of bronze-, silver-, and goldsmiths</li>
</ul>
</p>

<h3>Burials</h3>
<!-- TODO: tumuli burial goes here ("https://en.wikipedia.org/wiki/Hochdorf_Chieftain%27s_Grave") -->
<p>Important persons had large graves, sometimes a chamber with a hill was build upon them called a <a href="..\literature\term_definitions.php">tumuli</a>. Inside of these there were weapons, pottery, jewellery, and other items of importance. Occasionally, there were chariot burials, where a chariot - with the horses - was buried with them.</p>

<h3>Trade</h3>
<p>Trade with Greece was common, proven through finding <a href="">black ink art</a> at burial sites. Trade items included amber, ivory, and wine.</p>

